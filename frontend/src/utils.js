export const normalizeAvatarUrl = (url) => {
  // Если URL пустой (null, undefined, ''), возвращаем пустую строку
  if (!url) {
    return '';
  }

  // Если это уже полный URL (начинается с http), оставляем его как есть
  if (url.startsWith('http')) {
    return url;
  }

  // Для всех остальных случаев (локальных путей) мы гарантируем,
  // что путь начинается с одного слэша (/), создавая правильный
  // корневой путь, который поймет браузер.
  return `/${url.replace(/^\/+/, '')}`;
};